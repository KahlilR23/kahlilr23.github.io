function min(num1, num2) {
 var ans = num2;
  if (num1 < num2)
    ans = num1;
  return ans;
}


////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {

  if (num == 0)
    return true;
  if (num == 1)
    return false;
  if (num < 0)
    return false;
  else return isEven(num - 2);
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(str, char) {
  
  var count = 0; 
  for (var i = 0; i < str.length; i += 1) {
    if (str.charAt(i) === char){
      count += 1;
    }
  }
  return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(b) {
  
  var count = 0; 
  for (var i = 0; i < b.length; i += 1) {
    if (b.charAt(i) === "B")
      count += 1;
  }
  return count;

}

