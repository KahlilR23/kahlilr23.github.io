function drawChessboard(num) {


var totalSize = (num * num) + num;
var grid = "";
for (var i = 0; i <= totalSize; i++) {
  if (i % (num + 1) == 0)
    grid += "\n";
  else if (i % 2 == 0)
    grid += "#";
  else
    grid += " ";
}
console.log(grid);

}

